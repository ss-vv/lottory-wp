package com.unison.lottery.weibo.dataservice.commons.model;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class CountryDetailModel {

	private Double countryId;
	
	private String countryOrAreaName;
	
	private Double customOne;
	
	private String customTwo;

	

	public Double getCountryId() {
		return countryId;
	}

	public void setCountryId(Double countryId) {
		this.countryId = countryId;
	}

	public String getCountryOrAreaName() {
		return countryOrAreaName;
	}

	public void setCountryOrAreaName(String countryOrAreaName) {
		this.countryOrAreaName = countryOrAreaName;
	}

	
	
	public Double getCustomOne() {
		return customOne;
	}

	public void setCustomOne(Double customOne) {
		this.customOne = customOne;
	}

	

	public String getCustomTwo() {
		return customTwo;
	}

	public void setCustomTwo(String customTwo) {
		this.customTwo = customTwo;
	}

	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}
}
