CREATE TABLE `hot_and_recommend_match` (
	`id` BIGINT NOT NULL AUTO_INCREMENT,
	`matchId` BIGINT NOT NULL COMMENT '比赛id',
	`lottery` VARCHAR(50) NOT NULL COMMENT '彩种',
	`recommendCount` VARCHAR(50) NOT NULL COMMENT '推荐单数',
	`hostTeamName` VARCHAR(100) NOT NULL COMMENT '主队名',
	`guestTeamName` VARCHAR(100) NOT NULL COMMENT '客队名',
	`hostTeamPosition` VARCHAR(100)  NULL COMMENT '主队排名',
	`guestTeamPostion` VARCHAR(100)  NULL COMMENT '客队排名',
	`leagueName` VARCHAR(200)  NULL COMMENT '联赛名称',
	PRIMARY KEY (`id`)
)
COMMENT='热门推荐赛事表'
COLLATE='utf8_general_ci'
ENGINE=InnoDB
;
