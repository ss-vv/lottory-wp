package com.unison.lottery.weibo.msg.service.johm.exception;

/**
 * 微博程序运行异常.
 * 
 * @author Yang Bo
 */
public class WeiboRuntimeException extends RuntimeException {

	private static final long serialVersionUID = -1763845228541455341L;

	public WeiboRuntimeException() {
		super();
	}

	public WeiboRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public WeiboRuntimeException(String message) {
		super(message);
	}

	public WeiboRuntimeException(Throwable cause) {
		super(cause);
	}

}
