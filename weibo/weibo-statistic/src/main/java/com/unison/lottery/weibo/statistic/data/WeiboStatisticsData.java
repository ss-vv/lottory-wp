package com.unison.lottery.weibo.statistic.data;

import java.text.DateFormat;
import java.text.SimpleDateFormat;
import java.util.Date;

public class WeiboStatisticsData {
	/**统计日期eg: 2014-09-01*/
	private String statisticDate;
	private long weiboCount;
	private long officialWeiboCount;
	
	private long commentCount;
	private long officialUserCommentCount;
	private long normalUserCommentCount;
	private long officialUserBeCommentedCount;
	private long normalUserBeCommentedCount;
	
	private long praiseCount;
	private long officialUserPraiseCount;
	private long normalUserPraiseCount;
	private long officialUserBePraisedCount;
	private long normalUserBePraisedCount;

	private long betCount;
	private long officialUserBeFollowCount;
	private long betMountCount;
	private long officialUserBeFollowedMountCount;
	
	public WeiboStatisticsData(long date){
		DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
		this.statisticDate = dateFormat.format(new Date(date));
	}
	public String getStatisticDate() {
		return statisticDate;
	}
	public void setStatisticDate(String statisticDate) {
		this.statisticDate = statisticDate;
	}
	public long getWeiboCount() {
		return weiboCount;
	}
	public void setWeiboCount(long weiboCount) {
		this.weiboCount = weiboCount;
	}
	public long getOfficialWeiboCount() {
		return officialWeiboCount;
	}
	public void setOfficialWeiboCount(long officialWeiboCount) {
		this.officialWeiboCount = officialWeiboCount;
	}
	public long getCommentCount() {
		return commentCount;
	}
	public void setCommentCount(long commentCount) {
		this.commentCount = commentCount;
	}
	public long getOfficialUserCommentCount() {
		return officialUserCommentCount;
	}
	public void setOfficialUserCommentCount(long officialUserCommentCount) {
		this.officialUserCommentCount = officialUserCommentCount;
	}
	public long getNormalUserCommentCount() {
		return normalUserCommentCount;
	}
	public void setNormalUserCommentCount(long normalUserCommentCount) {
		this.normalUserCommentCount = normalUserCommentCount;
	}
	public long getOfficialUserBeCommentedCount() {
		return officialUserBeCommentedCount;
	}
	public void setOfficialUserBeCommentedCount(long officialUserBeCommentedCount) {
		this.officialUserBeCommentedCount = officialUserBeCommentedCount;
	}
	public long getNormalUserBeCommentedCount() {
		return normalUserBeCommentedCount;
	}
	public void setNormalUserBeCommentedCount(long normalUserBeCommentedCount) {
		this.normalUserBeCommentedCount = normalUserBeCommentedCount;
	}
	public long getPraiseCount() {
		return praiseCount;
	}
	public void setPraiseCount(long praiseCount) {
		this.praiseCount = praiseCount;
	}
	public long getOfficialUserPraiseCount() {
		return officialUserPraiseCount;
	}
	public void setOfficialUserPraiseCount(long officialUserPraiseCount) {
		this.officialUserPraiseCount = officialUserPraiseCount;
	}
	public long getNormalUserPraiseCount() {
		return normalUserPraiseCount;
	}
	public void setNormalUserPraiseCount(long normalUserPraiseCount) {
		this.normalUserPraiseCount = normalUserPraiseCount;
	}
	public long getOfficialUserBePraisedCount() {
		return officialUserBePraisedCount;
	}
	public void setOfficialUserBePraisedCount(long officialUserBePraisedCount) {
		this.officialUserBePraisedCount = officialUserBePraisedCount;
	}
	public long getNormalUserBePraisedCount() {
		return normalUserBePraisedCount;
	}
	public void setNormalUserBePraisedCount(long normalUserBePraisedCount) {
		this.normalUserBePraisedCount = normalUserBePraisedCount;
	}
	public long getBetCount() {
		return betCount;
	}
	public void setBetCount(long betCount) {
		this.betCount = betCount;
	}
	public long getOfficialUserBeFollowCount() {
		return officialUserBeFollowCount;
	}
	public void setOfficialUserBeFollowCount(long officialUserBeFollowCount) {
		this.officialUserBeFollowCount = officialUserBeFollowCount;
	}
	public long getBetMountCount() {
		return betMountCount;
	}
	public void setBetMountCount(long betMountCount) {
		this.betMountCount = betMountCount;
	}
	public long getOfficialUserBeFollowedMountCount() {
		return officialUserBeFollowedMountCount;
	}
	public void setOfficialUserBeFollowedMountCount(
			long officialUserBeFollowedMountCount) {
		this.officialUserBeFollowedMountCount = officialUserBeFollowedMountCount;
	}
}
