package com.unison.lottery.weibo.data.service.store.persist.entity;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * 博彩公司
 * 
 * @author Yang Bo
 */
@Entity
@Table(name = "md_lottery_corp")
public class LotteryCorpPO {
	
	@Id
	private long id;
	private String name;
	private long eliteId;
	private String eliteName;
	private Date createTime;
	private Date updateTime;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getEliteId() {
		return eliteId;
	}
	public void setEliteId(long eliteId) {
		this.eliteId = eliteId;
	}
	public String getEliteName() {
		return eliteName;
	}
	public void setEliteName(String eliteName) {
		this.eliteName = eliteName;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
}
