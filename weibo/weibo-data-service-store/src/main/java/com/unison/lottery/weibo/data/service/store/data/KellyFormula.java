package com.unison.lottery.weibo.data.service.store.data;

import java.math.BigDecimal;

public class KellyFormula {

	/**平均胜率*/
	private BigDecimal argWin;
	
	/**平均平率*/
	private BigDecimal argFlat;
	
	/**平均负率*/
	private BigDecimal argNegative;

	public BigDecimal getArgWin() {
		return argWin;
	}

	public void setArgWin(BigDecimal argWin) {
		this.argWin = argWin;
	}

	public BigDecimal getArgFlat() {
		return argFlat;
	}

	public void setArgFlat(BigDecimal argFlat) {
		this.argFlat = argFlat;
	}

	public BigDecimal getArgNegative() {
		return argNegative;
	}

	public void setArgNegative(BigDecimal argNegative) {
		this.argNegative = argNegative;
	}
}