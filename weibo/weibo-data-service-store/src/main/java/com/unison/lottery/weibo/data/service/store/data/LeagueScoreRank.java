package com.unison.lottery.weibo.data.service.store.data;

import java.util.List;
import com.unison.lottery.weibo.data.service.store.persist.entity.FBLeagueScorePO;

/**
 * @desc 积分排名的DTO对象
 * @author lei.li@unison.net.cn
 * @createTime 2014-2-21
 * @version 1.0
 */
public class LeagueScoreRank {

	/**
	 * 联赛名称
	 */
	private String leagueName;

	/**
	 * 联赛类型
	 */
	private MatchTypeEnum matchTypeEnum;

	/**
	 * 联赛积分排行列表
	 */
	private List<FBLeagueScorePO> leagueScoreList;

	/**
	 * 杯赛积分排行列表
	 */
	private List<Object[]> cupScoreList;

	public String getLeagueName() {
		return leagueName;
	}

	public void setLeagueName(String leagueName) {
		this.leagueName = leagueName;
	}

	public List<FBLeagueScorePO> getLeagueScoreList() {
		return leagueScoreList;
	}

	public void setLeagueScoreList(List<FBLeagueScorePO> leagueScoreList) {
		this.leagueScoreList = leagueScoreList;
	}

	public List<Object[]> getCupScoreList() {
		return cupScoreList;
	}

	public void setCupScoreList(List<Object[]> cupScoreList) {
		this.cupScoreList = cupScoreList;
	}

	public MatchTypeEnum getMatchTypeEnum() {
		return matchTypeEnum;
	}

	public void setMatchTypeEnum(MatchTypeEnum matchTypeEnum) {
		this.matchTypeEnum = matchTypeEnum;
	}
}