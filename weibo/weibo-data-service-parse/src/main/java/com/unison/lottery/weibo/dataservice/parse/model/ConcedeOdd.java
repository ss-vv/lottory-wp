package com.unison.lottery.weibo.dataservice.parse.model;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

/**
 * 让球赔率
 * @author u9
 *
 */
public class ConcedeOdd extends BaseOdd {

	private String initHandicap;
	private String homeInitOdd;
	private String guestInitOdd;
	private String immediateHandicap;
	private String homeImmediateOdd;
	private String guestImmediateOdd;

	public ConcedeOdd() {
		super();
	}

	public String getInitHandicap() {
		return initHandicap;
	}

	public void setInitHandicap(String initHandicap) {
		this.initHandicap = initHandicap;
	}

	public String getHomeInitOdd() {
		return homeInitOdd;
	}

	public void setHomeInitOdd(String homeInitOdd) {
		this.homeInitOdd = homeInitOdd;
	}

	public String getGuestInitOdd() {
		return guestInitOdd;
	}

	public void setGuestInitOdd(String guestInitOdd) {
		this.guestInitOdd = guestInitOdd;
	}

	public String getImmediateHandicap() {
		return immediateHandicap;
	}

	public void setImmediateHandicap(String immediateHandicap) {
		this.immediateHandicap = immediateHandicap;
	}

	public String getHomeImmediateOdd() {
		return homeImmediateOdd;
	}

	public void setHomeImmediateOdd(String homeImmediateOdd) {
		this.homeImmediateOdd = homeImmediateOdd;
	}

	public String getGuestImmediateOdd() {
		return guestImmediateOdd;
	}

	public void setGuestImmediateOdd(String guestImmediateOdd) {
		this.guestImmediateOdd = guestImmediateOdd;
	}
	
	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}

}