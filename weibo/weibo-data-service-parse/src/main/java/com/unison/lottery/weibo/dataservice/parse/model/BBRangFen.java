package com.unison.lottery.weibo.dataservice.parse.model;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class BBRangFen  extends ConcedeOdd{
	
	private boolean zouDi;//是否走地
	private String homeZouDiOdd;//主队走地赔率
	private String guestZouDiOdd;//客队走地赔率
	public boolean isZouDi() {
		return zouDi;
	}
	public void setZouDi(boolean zouDi) {
		this.zouDi = zouDi;
	}
	
	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}
	public String getHomeZouDiOdd() {
		return homeZouDiOdd;
	}
	public void setHomeZouDiOdd(String homeZouDiOdd) {
		this.homeZouDiOdd = homeZouDiOdd;
	}
	public String getGuestZouDiOdd() {
		return guestZouDiOdd;
	}
	public void setGuestZouDiOdd(String guestZouDiOdd) {
		this.guestZouDiOdd = guestZouDiOdd;
	}
		
	
}
