package com.unison.lottery.weibo.dataservice.parse.model;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class FBLeagueContentData {

	private String leagueId;
	private String colour;
	private BaseName leagueSimpleName;
	private BaseName leagueAllName;
	private String type;
	private String sumRound;
	private String currRound;
	private String currMatchSeason;
	private String countryID;
	private String country;
	//1欧洲联赛,2美洲联赛,3亚洲联赛,4大洋洲联赛,5非洲联赛
	private String areaID;
	public String getLeagueId() {
		return leagueId;
	}
	public void setLeagueId(String leagueId) {
		this.leagueId = leagueId;
	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	public BaseName getLeagueSimpleName() {
		return leagueSimpleName;
	}
	public void setLeagueSimpleName(BaseName leagueSimpleName) {
		this.leagueSimpleName = leagueSimpleName;
	}
	public BaseName getLeagueAllName() {
		return leagueAllName;
	}
	public void setLeagueAllName(BaseName leagueAllName) {
		this.leagueAllName = leagueAllName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSumRound() {
		return sumRound;
	}
	public void setSumRound(String sumRound) {
		this.sumRound = sumRound;
	}
	public String getCurrRound() {
		return currRound;
	}
	public void setCurrRound(String currRound) {
		this.currRound = currRound;
	}
	public String getCurrMatchSeason() {
		return currMatchSeason;
	}
	public void setCurrMatchSeason(String currMatchSeason) {
		this.currMatchSeason = currMatchSeason;
	}
	public String getCountryID() {
		return countryID;
	}
	public void setCountryID(String countryID) {
		this.countryID = countryID;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getAreaID() {
		return areaID;
	}
	public void setAreaID(String areaID) {
		this.areaID = areaID;
	}
	
	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}
}
