package com.unison.lottery.weibo.dataservice.parse.model;

import java.util.List;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class FBBFDetail {
	
	private List<FBEventContentData> eventContentDataList;

	private List<FBTechnicContentData> TechnicContentDataList;

	public List<FBEventContentData> getEventContentDataList() {
		return eventContentDataList;
	}

	public void setEventContentDataList(
			List<FBEventContentData> eventContentDataList) {
		this.eventContentDataList = eventContentDataList;
	}

	public List<FBTechnicContentData> getTechnicContentDataList() {
		return TechnicContentDataList;
	}

	public void setTechnicContentDataList(
			List<FBTechnicContentData> technicContentDataList) {
		TechnicContentDataList = technicContentDataList;
	}
	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}

}
