package com.unison.lottery.weibo.dataservice.parse.model;

import java.util.List;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class BBTeamData {
	
	private List<BBTeamContentData> bbTeamContentDataList;

	
	public String toString(){
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}


	public List<BBTeamContentData> getBbTeamContentDataList() {
		return bbTeamContentDataList;
	}


	public void setBbTeamContentDataList(List<BBTeamContentData> bbTeamContentDataList) {
		this.bbTeamContentDataList = bbTeamContentDataList;
	}
}
