package com.unison.lottery.newsextract.data;

public class ExtractNews {
	
	private String id;
	
	/**	新闻标题 */
	private String title;
	
	/**	新闻内容 */
	private String content;
	
	/**	该新闻链接 URL 地址 */
	private String url;
	
	/** 来源 URL or 源网站名称*/
	private String source;
	
	private String newsType; 
	
	private String relativeTeamId;
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getNewsType() {
		return newsType;
	}

	public void setNewsType(String newsType) {
		this.newsType = newsType;
	}

	public String getRelativeTeamId() {
		return relativeTeamId;
	}

	public void setRelativeTeamId(String relativeTeamId) {
		this.relativeTeamId = relativeTeamId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
