package com.unison.lottery.weibo.data;

import java.util.Date;

/**
 * @Description:赛事数据
 * @author 江浩翔
 * @date 2014-2-11 上午11:17:30
 * @version V1.0
 */
public class MatchData {

	private long id;

	/** 赛事名称 */
	private String matchName;

	/** 比赛日期 */
	private Date matchDate;

	/** 主队名称 */
	private String homeTeam;

	/** 客队名称 */
	private String guestTeam;

	/** 半场比分 eg:"1:0" */
	private String halfScore;

	/** 全场比分 eg:"1:0" */
	private String score;

	/** 盘口 */
	private float pankou;

	/** 胜负 value: "胜" "平" "负" */
	private String matchResult;

	/** 盘路 value: true-"赢" false-"输" */
	private boolean panlu;

	/** 大小 value: true-"大" false-"小" */
	private boolean bigOrSmall;

	// 让分赔率的大小
	private String bigSmall;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getMatchName() {
		return matchName;
	}

	public void setMatchName(String matchName) {
		this.matchName = matchName;
	}

	public Date getMatchDate() {
		return matchDate;
	}

	public void setMatchDate(Date matchDate) {
		this.matchDate = matchDate;
	}

	public String getHomeTeam() {
		return homeTeam;
	}

	public void setHomeTeam(String homeTeam) {
		this.homeTeam = homeTeam;
	}

	public String getGuestTeam() {
		return guestTeam;
	}

	public void setGuestTeam(String guestTeam) {
		this.guestTeam = guestTeam;
	}

	public String getHalfScore() {
		return halfScore;
	}

	public void setHalfScore(String halfScore) {
		this.halfScore = halfScore;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public float getPankou() {
		return pankou;
	}

	public void setPankou(float pankou) {
		this.pankou = pankou;
	}

	public String getMatchResult() {
		return matchResult;
	}

	public void setMatchResult(String matchResult) {
		this.matchResult = matchResult;
	}

	public boolean isPanlu() {
		return panlu;
	}

	public void setPanlu(boolean panlu) {
		this.panlu = panlu;
	}

	public boolean isBigOrSmall() {
		return bigOrSmall;
	}

	public void setBigOrSmall(boolean bigOrSmall) {
		this.bigOrSmall = bigOrSmall;
	}

	public String getBigSmall() {
		return bigSmall;
	}

	public void setBigSmall(String bigSmall) {
		this.bigSmall = bigSmall;
	}
}
