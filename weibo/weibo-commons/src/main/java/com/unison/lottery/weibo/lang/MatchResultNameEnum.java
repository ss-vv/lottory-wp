package com.unison.lottery.weibo.lang;

/**
 * @desc 赛果名称的枚举类
 * @author lei.li@unison.net.cn
 * @createTime 2014-3-21
 * @version 1.0
 */
public enum MatchResultNameEnum {

	SHENG("胜", 3), PING("平", 1), FU("负", 0);

	MatchResultNameEnum(String name, int value) {
		this.name = name;
		this.value = value;
	}

	private String name;

	private int value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
}
