package com.unison.lottery.weibo.data;

import java.io.Serializable;
import java.util.Date;

import org.apache.commons.lang.builder.ReflectionToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class HomeMatchRecommend implements Serializable {

	private static final long serialVersionUID = 4442743671945283738L;
	
	private long id;

	/** 彩种名称 */
	private String lotteryName;

	/** 推荐微博用户ID */
	private long weiboUserId;
	
	/**推荐微博用户昵称*/
	private String nickname;
	
	/**微博ID*/
	private long weiboMsgId;
	
	/** 今日赛事数 */
	private int matchCount;

	/**联赛名称*/
	private String leagueName;

	/**比赛时间*/
	private Date matchTime;

	/** 投注截至时间 */
	private Date deadlineTime;

	/** 主队名称 */
	private String homeTeamName;

	/** 客队名称 */
	private String guestTeamName;

	/** 玩法类型 */
	private String playType;

	/**玩法赔率*/
	private String odds;

	/** 推荐选项 */
	private String recoOptions;
	
	/** 赛事注解 */
	private String annotation;

	/** 评论数量 */
	private int commentCount;

	/** 赞的数量 */
	private int pariseCount;

	@Override
	public String toString() {
		return ReflectionToStringBuilder.toString(this,
				ToStringStyle.MULTI_LINE_STYLE);
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
	
	public long getWeiboMsgId() {
		return weiboMsgId;
	}

	public void setWeiboMsgId(long weiboMsgId) {
		this.weiboMsgId = weiboMsgId;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	public String getLotteryName() {
		return lotteryName;
	}

	public void setLotteryName(String lotteryName) {
		this.lotteryName = lotteryName;
	}

	public long getWeiboUserId() {
		return weiboUserId;
	}

	public void setWeiboUserId(long weiboUserId) {
		this.weiboUserId = weiboUserId;
	}

	public int getMatchCount() {
		return matchCount;
	}

	public void setMatchCount(int matchCount) {
		this.matchCount = matchCount;
	}

	public String getLeagueName() {
		return leagueName;
	}

	public void setLeagueName(String leagueName) {
		this.leagueName = leagueName;
	}

	public Date getMatchTime() {
		return matchTime;
	}

	public void setMatchTime(Date matchTime) {
		this.matchTime = matchTime;
	}

	public Date getDeadlineTime() {
		return deadlineTime;
	}

	public void setDeadlineTime(Date deadlineTime) {
		this.deadlineTime = deadlineTime;
	}

	public String getHomeTeamName() {
		return homeTeamName;
	}

	public void setHomeTeamName(String homeTeamName) {
		this.homeTeamName = homeTeamName;
	}

	public String getGuestTeamName() {
		return guestTeamName;
	}

	public void setGuestTeamName(String guestTeamName) {
		this.guestTeamName = guestTeamName;
	}

	public String getPlayType() {
		return playType;
	}

	public void setPlayType(String playType) {
		this.playType = playType;
	}
	
	public String getOdds() {
		return odds;
	}

	public void setOdds(String odds) {
		this.odds = odds;
	}

	public String getRecoOptions() {
		return recoOptions;
	}

	public void setRecoOptions(String recoOptions) {
		this.recoOptions = recoOptions;
	}

	public String getAnnotation() {
		return annotation;
	}

	public void setAnnotation(String annotation) {
		this.annotation = annotation;
	}

	public int getCommentCount() {
		return commentCount;
	}

	public void setCommentCount(int commentCount) {
		this.commentCount = commentCount;
	}

	public int getPariseCount() {
		return pariseCount;
	}

	public void setPariseCount(int pariseCount) {
		this.pariseCount = pariseCount;
	}
}