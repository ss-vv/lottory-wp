package com.unison.lottery.weibo.data.service.store.persist.entity;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
@Entity
@Table(name="lt_redenvalope")
public class RedEnvalopePO implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6146571597958397753L;
	@Id
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	private Long envalopeId;
	private Long redEnvalopeAmount;
	private Long ltUserId;
	private Date createTime;
	private Integer envalopeNum;
	private String smallEnvalope;
	private String status;
	private Long grabedEnvalopeAmount;
	private Long amountReturnUserAccount;
	private Integer secondOfGrabed;
	public Long getEnvalopeId() {
		return envalopeId;
	}
	public void setEnvalopeId(Long envalopeId) {
		this.envalopeId = envalopeId;
	}
	public Long getRedEnvalopeAmount() {
		return redEnvalopeAmount;
	}
	public void setRedEnvalopeAmount(Long redEnvalopeAmount) {
		this.redEnvalopeAmount = redEnvalopeAmount;
	}
	
	
	public Integer getSecondOfGrabed() {
		return secondOfGrabed;
	}
	public void setSecondOfGrabed(Integer secondOfGrabed) {
		this.secondOfGrabed = secondOfGrabed;
	}
	public Long getLtUserId() {
		return ltUserId;
	}
	public void setLtUserId(Long ltUserId) {
		this.ltUserId = ltUserId;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getEnvalopeNum() {
		return envalopeNum;
	}
	public void setEnvalopeNum(Integer envalopeNum) {
		this.envalopeNum = envalopeNum;
	}
	public String getSmallEnvalope() {
		return smallEnvalope;
	}
	public void setSmallEnvalope(String smallEnvalope) {
		this.smallEnvalope = smallEnvalope;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Long getGrabedEnvalopeAmount() {
		return grabedEnvalopeAmount;
	}
	public void setGrabedEnvalopeAmount(Long grabedEnvalopeAmount) {
		this.grabedEnvalopeAmount = grabedEnvalopeAmount;
	}
	public Long getAmountReturnUserAccount() {
		return amountReturnUserAccount;
	}
	public void setAmountReturnUserAccount(Long amountReturnUserAccount) {
		this.amountReturnUserAccount = amountReturnUserAccount;
	}
}
