package com.xhcms.lottery.commons.data.repeat;


/**
 * @desc 追号计划完成进度
 * @createTime 2013-8-29
 * @author lei.li@unison.net.cn
 * @version 1.0
 */
public class RepeatPlanSchedule {

	//总期数
	private int totalIssue;
	
	//已完成期数
	private int completeIssue;
	
	public int getTotalIssue() {
		return totalIssue;
	}

	public void setTotalIssue(int totalIssue) {
		this.totalIssue = totalIssue;
	}

	public int getCompleteIssue() {
		return completeIssue;
	}

	public void setCompleteIssue(int completeIssue) {
		this.completeIssue = completeIssue;
	}
}
