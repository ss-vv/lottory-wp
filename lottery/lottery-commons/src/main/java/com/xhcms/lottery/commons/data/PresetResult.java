package com.xhcms.lottery.commons.data;

import java.util.List;

public class PresetResult {
	
	private String issueNumber;
	private List<PresetResultPerPlayId> presetResultPerPlayIdList;
	

	public String getIssueNumber() {
		return issueNumber;
	}
	public void setIssueNumber(String issueNumber) {
		this.issueNumber = issueNumber;
	}
	
	public List<PresetResultPerPlayId> getPresetResultPerPlayIdList() {
		return presetResultPerPlayIdList;
	}
	public void setPresetResultPerPlayIdList(
			List<PresetResultPerPlayId> presetResultPerPlayIdList) {
		this.presetResultPerPlayIdList = presetResultPerPlayIdList;
	}
	

}
