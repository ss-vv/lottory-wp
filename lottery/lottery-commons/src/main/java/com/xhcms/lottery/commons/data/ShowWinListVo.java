package com.xhcms.lottery.commons.data;
/**
 * 晒单助人vo类
 * @author haohao
 *
 */
public class ShowWinListVo {

	private Double bonus_;//总金额
	private String scheme;//总方案数
	private Long sponsorId;
	private Long weiboUserId;
	private String headImageURL;
	private String nickName;
	private String winSchemeCount;//盈利单数
	private Double winRate;//盈利率
	private String followSchemeCount;
	private String winCount;
	private String topType;

	public Double getBonus_() {
		return bonus_;
	}
	public void setBonus_(Double bonus_) {
		this.bonus_ = bonus_;
	}
	public String getScheme() {
		return scheme;
	}
	public void setScheme(String scheme) {
		this.scheme = scheme;
	}
	public Long getSponsorId() {
		return sponsorId;
	}
	public void setSponsorId(Long sponsorId) {
		this.sponsorId = sponsorId;
	}
	public Long getWeiboUserId() {
		return weiboUserId;
	}
	public void setWeiboUserId(Long weiboUserId) {
		this.weiboUserId = weiboUserId;
	}
	public String getWinSchemeCount() {
		return winSchemeCount;
	}
	public void setWinSchemeCount(String winSchemeCount) {
		this.winSchemeCount = winSchemeCount;
	}
	public Double getWinRate() {
		return winRate;
	}
	public void setWinRate(Double winRate) {
		this.winRate = winRate;
	}
	public String getHeadImageURL() {
		return headImageURL;
	}
	public void setHeadImageURL(String headImageURL) {
		this.headImageURL = headImageURL;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getFollowSchemeCount() {
		return followSchemeCount;
	}
	public void setFollowSchemeCount(String followSchemeCount) {
		this.followSchemeCount = followSchemeCount;
	}
	public String getWinCount() {
		return winCount;
	}
	public void setWinCount(String winCount) {
		this.winCount = winCount;
	}
	public String getTopType() {
		return topType;
	}
	public void setTopType(String topType) {
		this.topType = topType;
	}
	
}
