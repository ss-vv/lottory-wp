package com.xhcms.lottery.commons.data;

import java.io.Serializable;
import java.math.BigDecimal;

public class BBMatchPlay implements Serializable{
    private static final long serialVersionUID = 7164295336747299458L;

    private String id;
	private Long matchId;
	private String playId;
	private String options;
	private String odds;
	private String priorOdds;
	private int status;
	private BigDecimal winBonus;
	private String winOption;
	private float defaultScore;
	private BBMatch match;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getMatchId() {
		return matchId;
	}

	public void setMatchId(Long matchId) {
		this.matchId = matchId;
	}

	public String getPlayId() {
		return playId;
	}

	public void setPlayId(String playId) {
		this.playId = playId;
	}

	public String getOptions() {
		return options;
	}

	public void setOptions(String options) {
		this.options = options;
	}

	public String getOdds() {
		return odds;
	}

	public void setOdds(String odds) {
		this.odds = odds;
	}

	public String getPriorOdds() {
		return priorOdds;
	}

	public void setPriorOdds(String priorOdds) {
		this.priorOdds = priorOdds;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public BigDecimal getWinBonus() {
		return winBonus;
	}

	public void setWinBonus(BigDecimal winBonus) {
		this.winBonus = winBonus;
	}

	public String getWinOption() {
		return winOption;
	}

	public void setWinOption(String winOption) {
		this.winOption = winOption;
	}

    public float getDefaultScore() {
        return defaultScore;
    }

    public void setDefaultScore(float defaultScore) {
        this.defaultScore = defaultScore;
    }

    public BBMatch getMatch() {
        return match;
    }

    public void setMatch(BBMatch match) {
        this.match = match;
    }

}
