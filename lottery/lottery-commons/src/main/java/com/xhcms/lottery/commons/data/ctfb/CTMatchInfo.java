package com.xhcms.lottery.commons.data.ctfb;

import java.util.List;
import com.xhcms.lottery.commons.data.CTFBMatch;
import com.xhcms.lottery.commons.data.IssueInfo;

/**
 * 传统足彩投注赛事信息
 * @author Wang Lei
 *
 */
public class CTMatchInfo {
	
	/** 代码 */
	int code;
	
	/** 消息 */
	String message;
	
	/** 目标期信息 */
	IssueInfo issueInfo;
	
	/** 在售期信息列表 */
	List<IssueInfo> issueInfos;
	
	/** 历史期信息列表 */
	List<IssueInfo> oldIssueInfos;
	
	/** 目标期信息对应的传统足彩赛事列表 */
	List<CTFBMatch> ctFBMatchs;

	public IssueInfo getIssueInfo() {
		return issueInfo;
	}

	public void setIssueInfo(IssueInfo issueInfo) {
		this.issueInfo = issueInfo;
	}

	public List<CTFBMatch> getCtFBMatchs() {
		return ctFBMatchs;
	}

	public void setCtFBMatchs(List<CTFBMatch> ctFBMatchs) {
		this.ctFBMatchs = ctFBMatchs;
	}

	public List<IssueInfo> getIssueInfos() {
		return issueInfos;
	}

	public void setIssueInfos(List<IssueInfo> issueInfos) {
		this.issueInfos = issueInfos;
	}

	public List<IssueInfo> getOldIssueInfos() {
		return oldIssueInfos;
	}

	public void setOldIssueInfos(List<IssueInfo> oldIssueInfos) {
		this.oldIssueInfos = oldIssueInfos;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
