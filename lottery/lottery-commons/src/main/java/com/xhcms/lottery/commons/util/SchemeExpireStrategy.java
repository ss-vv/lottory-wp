package com.xhcms.lottery.commons.util;

public class SchemeExpireStrategy {

	/** 未达到最终状态方案的存活时间（单位：秒）;默认值一天 */
	private int expireSec = 60 * 60 * 24 * 1;

	/** 最终状态方案的存活时间（单位：秒） ;默认值七天*/
	private int finalStatusExpireSec = 60 * 60 * 24 * 7;

	public int getExpireSec() {
		return expireSec;
	}

	public void setExpireSec(int expireSec) {
		this.expireSec = expireSec;
	}

	public int getFinalStatusExpireSec() {
		return finalStatusExpireSec;
	}

	public void setFinalStatusExpireSec(int finalStatusExpireSec) {
		this.finalStatusExpireSec = finalStatusExpireSec;
	}
}