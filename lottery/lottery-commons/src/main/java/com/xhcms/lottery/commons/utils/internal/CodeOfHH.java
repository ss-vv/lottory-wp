package com.xhcms.lottery.commons.utils.internal;

import com.xhcms.lottery.lang.MixPlayType;

/**
 * 混合过关玩法的投注代码。
 * 
 * @author Yang Bo
 *
 */
public class CodeOfHH {
	
	/** 投注内容 */
	private StringBuilder codeBuf;
	
	/** 是否只有一种玩法 */
	private boolean isOnlyOnePlay = false;
	
	/** 玩法, isOnlyOnePlay=true 时有效 */
	private MixPlayType play;

	public StringBuilder getCodeBuf() {
		return codeBuf;
	}

	public void setCodeBuf(StringBuilder codeBuf) {
		this.codeBuf = codeBuf;
	}

	public void setPlay(MixPlayType play) {
		this.play = play;
	}

	public boolean isOnlyOnePlay() {
		return isOnlyOnePlay;
	}

	public void setOnlyOnePlay(boolean isOnlyOnePlay) {
		this.isOnlyOnePlay = isOnlyOnePlay;
	}

	public MixPlayType getPlay() {
		return play;
	}

}
