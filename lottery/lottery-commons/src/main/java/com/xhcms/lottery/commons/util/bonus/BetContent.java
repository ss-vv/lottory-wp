package com.xhcms.lottery.commons.util.bonus;

import java.math.BigDecimal;

import org.apache.commons.lang.builder.ReflectionToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class BetContent {
	
	 	private Long matchId;

	    private String code;
	    
	    private String cnCode;
	    
	    private BigDecimal odd;
	    
	    /** 胆码 */
	    protected boolean seed;
	    
	    /**默认比分（让球、预设总分）*/
	    protected float defaultScore;
	    
	    protected String playId;
	    
	    private String betOption;

		public Long getMatchId() {
			return matchId;
		}

		public void setMatchId(Long matchId) {
			this.matchId = matchId;
		}

		public String getCode() {
			return code;
		}

		public void setCode(String code) {
			this.code = code;
		}

		public String getCnCode() {
			return cnCode;
		}

		public void setCnCode(String cnCode) {
			this.cnCode = cnCode;
		}

		public BigDecimal getOdd() {
			return odd;
		}

		public void setOdd(BigDecimal odd) {
			this.odd = odd;
		}

		public boolean isSeed() {
			return seed;
		}

		public void setSeed(boolean seed) {
			this.seed = seed;
		}

		public float getDefaultScore() {
			return defaultScore;
		}

		public void setDefaultScore(float defaultScore) {
			this.defaultScore = defaultScore;
		}

		public String getPlayId() {
			return playId;
		}

		public void setPlayId(String playId) {
			this.playId = playId;
		}

		public String getBetOption() {
			return betOption;
		}

		public void setBetOption(String betOption) {
			this.betOption = betOption;
		}

	@Override
	public String toString() {
		return ReflectionToStringBuilder.toString(this, ToStringStyle.MULTI_LINE_STYLE);
	}
}
