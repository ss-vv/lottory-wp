package com.xhcms.lottery.commons.data;

import java.io.Serializable;
import java.util.Date;

import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class MatchPlatform implements Serializable {
	private static final long serialVersionUID = -8830057762314589080L;
	private Long id;
	private String platformId;
	private String lotteryId;
	private Long matchId;
	private Long platformMatchId;
	private Date createdTime;

	public Date getCreatedTime() {
		return createdTime;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getPlatformId() {
		return platformId;
	}

	public void setPlatformId(String platformId) {
		this.platformId = platformId;
	}

	public String getLotteryId() {
		return lotteryId;
	}

	public void setLotteryId(String lotteryId) {
		this.lotteryId = lotteryId;
	}

	public Long getMatchId() {
		return matchId;
	}

	public void setMatchId(Long matchId) {
		this.matchId = matchId;
	}

	public Long getPlatformMatchId() {
		return platformMatchId;
	}

	public void setPlatformMatchId(Long platformMatchId) {
		this.platformMatchId = platformMatchId;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}
	public String toString(){
		return ReflectionToStringBuilder.toString(this, ToStringStyle.MULTI_LINE_STYLE);
	}
}