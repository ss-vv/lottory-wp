package com.xhcms.lottery.lang;

/**
 * @desc 追号期计划是否有效
 * @createTime 2013-8-5
 * @author lei.li@unison.net.cn
 * @version 1.0
 */
public enum RepeatIssuePlanIsValid {
	
	INVALID(false, "invalid"),	//无效
	VALID(true, "valid");		//有效
	
	private boolean type;
	
	private String name;
	
	private RepeatIssuePlanIsValid(boolean type, String name) {
		this.type = type;
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public boolean isType() {
		return type;
	}

	public void setType(boolean type) {
		this.type = type;
	}
}
