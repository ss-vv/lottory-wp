package com.xhcms.lottery.pb.xml.model;

import javax.xml.bind.annotation.XmlAttribute;

public class IssueInfoResp {
	private String lotteryType;
	private String currentIssueNumber;
	private String startTime;
	private String deadline;
	
	public String getLotteryType() {
		return lotteryType;
	}
	@XmlAttribute(name="lotteryType")
	public void setLotteryType(String lotteryType) {
		this.lotteryType = lotteryType;
	}
	public String getCurrentIssueNumber() {
		return currentIssueNumber;
	}
	@XmlAttribute(name="currentIssueNumber")
	public void setCurrentIssueNumber(String currentIssueNumber) {
		this.currentIssueNumber = currentIssueNumber;
	}
	public String getDeadline() {
		return deadline;
	}
	@XmlAttribute(name="deadline")
	public void setDeadline(String deadline) {
		this.deadline = deadline;
	}
	public String getStartTime() {
		return startTime;
	}
	@XmlAttribute(name="startTime")
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
}
