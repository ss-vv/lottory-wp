package com.xhcms.lottery.pb.xml.model;

import javax.xml.bind.annotation.XmlAttribute;

public class BetReq {
	private String lotteryType; 
	private String playType; 
	private String issueNumber; 
	private int betMoney;
	private String betContent; 
	private int multiple; 
	private String partnerUserId; 
	private String uuid;
	
	private String partnerId;//请求附加信息，由程序注入
	
	public String getLotteryType() {
		return lotteryType;
	}
	@XmlAttribute(name="lotteryType")
	public void setLotteryType(String lotteryType) {
		this.lotteryType = lotteryType;
	}
	public String getPlayType() {
		return playType;
	}
	@XmlAttribute(name="playType")
	public void setPlayType(String playType) {
		this.playType = playType;
	}
	public String getIssueNumber() {
		return issueNumber;
	}
	@XmlAttribute(name="issueNumber")
	public void setIssueNumber(String issueNumber) {
		this.issueNumber = issueNumber;
	}
	public int getBetMoney() {
		return betMoney;
	}
	@XmlAttribute(name="betMoney")
	public void setBetMoney(int betMoney) {
		this.betMoney = betMoney;
	}
	public String getBetContent() {
		return betContent;
	}
	@XmlAttribute(name="betContent")
	public void setBetContent(String betContent) {
		this.betContent = betContent;
	}
	public int getMultiple() {
		return multiple;
	}
	@XmlAttribute(name="multiple")
	public void setMultiple(int multiple) {
		this.multiple = multiple;
	}
	public String getPartnerUserId() {
		return partnerUserId;
	}
	@XmlAttribute(name="partnerUserId")
	public void setPartnerUserId(String partnerUserId) {
		this.partnerUserId = partnerUserId;
	}
	public String getUuid() {
		return uuid;
	}
	@XmlAttribute(name="uuid")
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(String partnerId) {
		this.partnerId = partnerId;
	}
}