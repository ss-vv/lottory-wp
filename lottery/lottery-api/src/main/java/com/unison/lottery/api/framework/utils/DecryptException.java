package com.unison.lottery.api.framework.utils;

public class DecryptException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1301839391800588339L;

	public DecryptException() {
		super();
		
	}

	public DecryptException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DecryptException(String arg0) {
		super(arg0);
		
	}

	public DecryptException(Throwable arg0) {
		super(arg0);
		
	}

}
