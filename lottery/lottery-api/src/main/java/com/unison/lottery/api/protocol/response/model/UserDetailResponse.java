package com.unison.lottery.api.protocol.response.model;

import java.util.List;

import com.xhcms.lottery.commons.data.Account;
import com.xhcms.lottery.commons.data.Recharge;
import com.xhcms.lottery.commons.data.Win;
import com.xhcms.lottery.commons.data.Withdraw;

public class UserDetailResponse extends HaveReturnStatusResponse{
	
	private List<Win> winList;
	private List<Withdraw> WithdrawList;
	private List<Recharge> rechargeList;
	
	private Account account;
	public List<Win> getWinList() {
		return winList;
	}
	public void setWinList(List<Win> winList) {
		this.winList = winList;
	}
	public List<Withdraw> getWithdrawList() {
		return WithdrawList;
	}
	public void setWithdrawList(List<Withdraw> withdrawList) {
		WithdrawList = withdrawList;
	}
	public List<Recharge> getRechargeList() {
		return rechargeList;
	}
	public void setRechargeList(List<Recharge> rechargeList) {
		this.rechargeList = rechargeList;
	}
	public Account getAccount() {
		return account;
	}
	public void setAccount(Account account) {
		this.account = account;
	}

}
