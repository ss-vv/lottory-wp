package com.unison.lottery.api.framework.log.model;

import com.unison.lottery.api.protocol.request.json.model.JsonMethodRequest;
import com.unison.lottery.api.protocol.request.xml.model.MethodRequest;
import com.unison.lottery.api.protocol.response.json.model.JsonResponse;
import com.unison.lottery.api.protocol.response.model.Response;


public class DetailLog {
	
	private MethodRequest requestObj;
	
	private JsonMethodRequest jsonRequestObj;
	
	private Response responseObj;
	
	private JsonResponse JsonResponseObj;

	
	public JsonMethodRequest getJsonRequestObj() {
		return jsonRequestObj;
	}

	public void setJsonRequestObj(JsonMethodRequest jsonRequestObj) {
		this.jsonRequestObj = jsonRequestObj;
	}

	public void setRequestObj(MethodRequest methodRequest) {
		requestObj=methodRequest;
		
	}

	/**
	 * @return the requestObj
	 */
	public MethodRequest getRequestObj() {
		return requestObj;
	}

	public void setResponseObj(Response responseObj) {
		this.responseObj = responseObj;
	}

	public Response getResponseObj() {
		return responseObj;
	}

	public JsonResponse getJsonResponseObj() {
		return JsonResponseObj;
	}

	public void setJsonResponseObj(JsonResponse jsonResponseObj) {
		JsonResponseObj = jsonResponseObj;
	}

	
}
