package com.xhcms.lottery.admin.persist.service.exception;

public class BetSchemeServiceException extends Exception {

	private static final long serialVersionUID = -791805595342606854L;

	public BetSchemeServiceException() {
		super();
	}

	public BetSchemeServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public BetSchemeServiceException(String message) {
		super(message);
	}

	public BetSchemeServiceException(Throwable cause) {
		super(cause);
	}
	
}
