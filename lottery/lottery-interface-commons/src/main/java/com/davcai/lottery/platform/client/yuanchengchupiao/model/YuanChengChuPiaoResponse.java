import org.apache.commons.lang3.builder.ReflectionToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

import com.davcai.lottery.platform.client.LotteryPlatformBaseResponse;

public class YuanChengChuPiaoResponse  extends LotteryPlatformBaseResponse{
	
	private String error;
	private String message;
	public YuanChengChuPiaoResponse() {
		
	}
	public YuanChengChuPiaoResponse(String error) {
		this.error = error;
	}
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	public String toString(){
		
		return ReflectionToStringBuilder.toString(this, ToStringStyle.MULTI_LINE_STYLE);
	}
	
}
